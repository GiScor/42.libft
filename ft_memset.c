#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*arr;
	size_t	i;

	arr = s;
	i = 0;
	while (i < n)
	{
		arr[i] = c;
		i++;
	}
	return (s);
}
